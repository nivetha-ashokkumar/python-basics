import math

first_number = int(input('enter first value'))
second_number = int(input('enter second value'))

print(math.sqrt(first_number))
print(math.pow(first_number, second_number))
print(math.ceil(second_number))
print(math.fabs(first_number))
print(math.factorial(first_number))
print(math.floor(first_number))
print(math.fmod(first_number, second_number))
print(math.gcd(first_number, second_number))




